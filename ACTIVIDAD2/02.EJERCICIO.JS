/*Un colegio necesita clasificar a sus estudiantes según su promedio. Crea un arreglo de objetos con 6
estudiantes, cada uno con nombre y promedio (definidos en el código). Recorre el arreglo con for...of y
muestra para cada estudiante su nombre, promedio y categoría: "EXCELENTE" si el promedio es mayor o
igual a 4.5, "BUENO" si está entre 3.5 y 4.4, "BÁSICO" si está entre 3.0 y 3.4, y "BAJO" si es menor a 3.0. Al
finalizar cuenta cuántos estudiantes hay en cada categoría y muéstralo en consola. Usa const para el
arreglo*/


console.log(" CLASIFICACIÓN DE ESTUDIANTES ");

// Arreglo de objetos
const estudiantes = [
    { nombre: "Andrés", promedio: 4.8 },
    { nombre: "María", promedio: 3.9 },
    { nombre: "Camila", promedio: 3.2 },
    { nombre: "Juan", promedio: 2.9 },
    { nombre: "Laura", promedio: 4.5 },
    { nombre: "Carlos", promedio: 3.6 }
];
// Contadores
let excelente = 0;
let bueno = 0;
let basico = 0;
let bajo = 0;
// Recorrer el arreglo
for (const estudiante of estudiantes) {
    let categoria = "";
    if (estudiante.promedio >= 4.5) {
        categoria = "EXCELENTE";
        excelente++;
    } else if (estudiante.promedio >= 3.5) {
        categoria = "BUENO";
        bueno++;
    } else if (estudiante.promedio >= 3.0) {
        categoria = "BÁSICO";
        basico++;
    } else {
        categoria = "BAJO";
        bajo++;
    }
    console.log("Nombre:", estudiante.nombre);
    console.log("Promedio:", estudiante.promedio);
    console.log("Categoría:", categoria);
    console.log("---------------------------");
}

console.log("RESUMEN");
console.log("Excelente:", excelente);
console.log("Bueno:", bueno);
console.log("Básico:", basico);
console.log("Bajo:", bajo);










































































































































































































































































































































































































































































































































































































































